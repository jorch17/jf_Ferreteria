    public void Conectar(){
         try{ 
            conexion=DriverManager.getConnection("jdbc:mysql://localhost/Ferre2","root","151617");                     
            st=conexion.createStatement();         
        }catch(SQLException err){ 
            JOptionPane.showMessageDialog(null,"Error "+err.getMessage()); 
        } 
    }
    private void entrar(){
        try{
            rs=st.executeQuery("select count(usuario) from usuarios where usuario= '"+this.jT_usuario.getText()+"' and password='"+this.jP_password.getText()+"';");
            if(rs.first()==true)  
                consulta = (rs.getInt(1));
            if(consulta == 1){
                JOptionPane.showMessageDialog(null, "Bienvenido", "Acceso válido", JOptionPane.INFORMATION_MESSAGE);
                Inicio jF_inicio = new Inicio();
                jF_inicio.setVisible(true);
                jF_inicio.setTitle("JP-Saf");
                jF_inicio.setLocationRelativeTo(null);
                this.setVisible(false);
            }
            else{
                JOptionPane.showMessageDialog(null, "Verificar datos", "Acceso denegado", JOptionPane.ERROR_MESSAGE);
            }
            
            }catch(SQLException err){ 
                JOptionPane.showMessageDialog(null,"Error "+err.getMessage()); 
        }
    }
